# tuple are enclose in () round brackets
# tuple are immutable
tuple1 = (10, 20, 30, 'Deven', "Microsoft")

print(tuple1)

# Tuple slicing 
print("tuple [2]" ,tuple1[2])

print("tuple [0:3]" ,tuple1[0:3])

print("tuple [2:]" ,tuple1[2:])

# We can use the slicing operator [] to extract items but we cannot change its value.

tuple1[0] = 10


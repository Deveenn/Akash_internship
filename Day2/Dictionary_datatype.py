# Dictionaries are enclosed by curly braces ({ }) 
Dict1 = {1:"Deven" ,2 : 10 ,3 : "Yash" , 'Harsh' : 30.5}

print(type(Dict1))
# how to use slicing in Dictionaries
print("Dict [1] =" , Dict1[1])

print("Dict [2] =" , Dict1[2])

# Here we called key 
print("Dict [Harsh] =" , Dict1['Harsh'])
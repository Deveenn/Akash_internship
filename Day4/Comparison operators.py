a = 10
b = 7
# equal to operator
print('a == b ', a == b)
# not equal to operator
print('a != b ', a != b)
# greater than operator
print('a > b ', a > b)
# less than operator
print('a < b ', a < b)
# greater than or equal to operator
print('a >= b ', a >= b)
# less than or equal to operator
print('a <= b ', a <= b)


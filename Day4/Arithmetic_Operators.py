x = 10
y = 5
# addition
print('Sum: ', x + y)
# subtraction
print('Subtraction: ', x - y)
# multiplication
print('Multiplication: ', x * y)
# division
print('Division: ', x / y)
# floor division
print('Floor Division: ', x // y)
# modulo
print('Modulo: ', x % y)
# x to the power y
print('Power: ', x ** y)
